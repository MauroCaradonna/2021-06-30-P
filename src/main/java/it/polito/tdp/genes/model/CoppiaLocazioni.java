package it.polito.tdp.genes.model;

public class CoppiaLocazioni {
	private String li;
	private String l2;
	
	public CoppiaLocazioni(String li, String l2) {
		super();
		this.li = li;
		this.l2 = l2;
	}
	public String getLi() {
		return li;
	}
	public void setLi(String li) {
		this.li = li;
	}
	public String getL2() {
		return l2;
	}
	public void setL2(String l2) {
		this.l2 = l2;
	}

}
